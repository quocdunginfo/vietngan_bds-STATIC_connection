    private void load_thongtin(int id)
    {
        DTO_USER obj=new BLL_USER().Get(id);
        if(obj==null) return;
        jTextField_id.setText(obj.id+"");
        jTextField_username.setText(obj.username);
        jTextField_fullname.setText(obj.fullname);
        jPasswordField.setText(obj.password);
        jPasswordField_re.setText(obj.password);
        jComboBox_role.setSelectedIndex(obj.role);
    }
    private void clear_thongtin()
    {
        jTextField_id.setText("");
        jTextField_username.setText("");
        jTextField_fullname.setText("");
        jPasswordField.setText("");
        jPasswordField_re.setText("");
        jComboBox_role.setSelectedIndex(0);
    }
    
