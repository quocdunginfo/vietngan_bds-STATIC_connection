    private void jButton_tk_timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_tk_timkiemActionPerformed
        // TODO add your handling code here:
        String username= UTL_TextField.GetString(jTextField_tk_username);
        String fullname= UTL_TextField.GetString(jTextField_tk_fullname);
        int role = jComboBox_tk_role.getSelectedIndex()-1;
        ArrayList<DTO_USER> list = new BLL_USER().TimKiem(-1, username, fullname, role, -1);
        ArrayList<Object> cols = new ArrayList<Object>();
        cols.add("Mã");
        cols.add("Tên đăng nhập");
        cols.add("Tên đầy đủ");
        cols.add("Vai trò");
        ArrayList<ArrayList<Object>> data = new ArrayList<ArrayList<Object>>();
        ArrayList<Object> row;
        for(DTO_USER tmp:list)
        {
             row = new ArrayList<Object>();
             row.add(tmp.id);
             row.add(tmp.username);
             row.add(tmp.fullname);
             row.add(tmp.role==0?"admin":(tmp.role==1?"user":""));
             data.add(row);
        }
        UTL_Table.SetTableData(jTable_ds, data, cols, false);
    }//GEN-LAST:event_jButton_tk_timkiemActionPerformed

