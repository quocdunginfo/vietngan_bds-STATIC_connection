    private void jButton_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_okActionPerformed
        // TODO add your handling code here:
        if(jTextField_id.getText().equals(""))
        {
            //mode thêm
            String mk1=String.valueOf(jPasswordField.getPassword());
            String mk2=String.valueOf(jPasswordField_re.getPassword());
            if(mk1.equals(mk2))
            {
                DTO_USER nd=new DTO_USER();
                nd.username=UTL_TextField.GetString(jTextField_username);
                nd.fullname=UTL_TextField.GetString(jTextField_fullname);
                nd.role=jComboBox_role.getSelectedIndex();
                new BLL_USER().Them(nd, mk1);
                JOptionPane.showMessageDialog(null, "Thêm thành công!");
                //refresh lại ds
                jButton_tk_timkiem.doClick();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Mật khẩu xác nhận chưa khớp!");
            }
        }
        else
        {
            //mode sửa
            String mk1=String.valueOf(jPasswordField.getPassword());
            String mk2=String.valueOf(jPasswordField_re.getPassword());
            if(mk1.equals(mk2))
            {
                DTO_USER nd=new BLL_USER().Get(UTL_TextField.GetInt(jTextField_id));
                if(!mk1.equals(nd.password))
                {
                    nd.password = UTL_String.MD5Hash(mk1);
                }
                nd.username=UTL_TextField.GetString(jTextField_username);
                nd.fullname=UTL_TextField.GetString(jTextField_fullname);
                nd.role=jComboBox_role.getSelectedIndex();
                new BLL_USER().Sua(nd);
                JOptionPane.showMessageDialog(null, "Sửa thành công!");
                //refresh lại ds
                jButton_tk_timkiem.doClick();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Mật khẩu xác nhận chưa khớp!");
            }
        }
    }//GEN-LAST:event_jButton_okActionPerformed

